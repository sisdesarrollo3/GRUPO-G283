let nota = 3;

if (nota >= 4.5) {
 console.log("Tienes una Excelente");
} else if (nota >= 4) {
 console.log("Tienes una Bueno");
} else if (nota >= 3) {
 console.log("Tienes una Regular");
} else if (nota >= 2) {
 console.log("Tienes una Deficiente");
} else {
 console.log("Tienes una Insuficiente");
}